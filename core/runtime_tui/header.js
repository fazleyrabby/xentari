// ---------- helpers ----------
function getTerminalWidth() {
  return process.stdout.columns || 80;
}

function getModeLabel(mode) {
  switch (mode) {
    case "strict": return "STRICT";
    case "sandbox": return "SANDBOX";
    default: return "NORMAL";
  }
}

// ---------- main ----------
